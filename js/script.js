const audio = document.getElementById("audio");
const ambient = document.getElementById("ambient");

const playBtn = document.getElementById("playBtn");
const ambientBtn = document.getElementById("ambientBtn");

const progress = document.getElementById("progress");
const progressContainer = document.getElementById("progressContainer");

const trackTitle = document.getElementById("trackTitle");
const currentTimeEl = document.getElementById("currentTime");
const durationEl = document.getElementById("duration");

let isPlaying = false;
let currentTrackIndex = 0;
let currentStoryIndex = 0;

/* 🎧 PLAY */
playBtn.addEventListener("click", () => {
  if (!audio.src) return;

  if (isPlaying) {
    audio.pause();
    playBtn.innerText = "▶";
  } else {
    audio.play();
    playBtn.innerText = "⏸";
  }

  isPlaying = !isPlaying;
});

/* 🌿 AMBIENTE */
ambientBtn.addEventListener("click", () => {
  if (ambient.paused) {
    ambient.volume = 0.3;
    ambient.play();
  } else {
    ambient.pause();
  }
});

/* ⏱ PROGRESSO */
audio.addEventListener("timeupdate", () => {
  const percent = (audio.currentTime / audio.duration) * 100;
  progress.style.width = percent + "%";

  currentTimeEl.innerText = formatTime(audio.currentTime);
  durationEl.innerText = formatTime(audio.duration);
});

/* ▶ AUTO PLAY PRÓXIMA FAIXA */
audio.addEventListener("ended", () => {
  currentTrackIndex++;

  // Reinicia se chegar ao fim da playlist
  if (currentTrackIndex >= tracks.length) {
    currentTrackIndex = 0;
  }

  selectTrack(currentTrackIndex);
});

/* 🧠 STORY AUTOMÁTICA DURANTE A MÚSICA */
setInterval(() => {
  if (isPlaying) {
    nextStory();
  }
}, 15000); // muda narrativa a cada 15 segundos

/* ⏩ SEEK */
progressContainer.addEventListener("click", (e) => {
  const width = progressContainer.clientWidth;
  audio.currentTime = (e.offsetX / width) * audio.duration;
});

/* 🎼 LISTA */
const trackList = document.getElementById("trackList");

tracks.forEach((track, index) => {
  const div = document.createElement("div");
  div.classList.add("track");

  div.innerHTML = `
    <h3>${track.title}</h3>
    <button onclick="selectTrack(${index})">▶ Tocar</button>
  `;

  trackList.appendChild(div);
});

function selectTrack(index) {
  currentTrackIndex = index;
  currentStoryIndex = 0;

  audio.src = tracks[index].src;
  trackTitle.innerText = tracks[index].title;

  audio.load();
  audio.play();

  isPlaying = true;
  playBtn.innerText = "⏸";

  updateStory();
}

/* 🧠 STORY */
function updateStory() {
  const story = tracks[currentTrackIndex].story;
  document.getElementById("storyText").innerText = story[currentStoryIndex];
}

function nextStory() {
  const story = tracks[currentTrackIndex].story;
  currentStoryIndex = (currentStoryIndex + 1) % story.length;
  updateStory();
}

/* 🎥 SCROLL */
function scrollToPlayer() {
  document.querySelector(".player-section").scrollIntoView({
    behavior: "smooth"
  });
}

/* 🕒 TIME */
function formatTime(time) {
  if (isNaN(time)) return "0:00";
  const min = Math.floor(time / 60);
  const sec = Math.floor(time % 60).toString().padStart(2, "0");
  return `${min}:${sec}`;
}

/* ✨ VAGA-LUMES */
const canvas = document.getElementById("fireflies");
const ctx = canvas.getContext("2d");

canvas.width = window.innerWidth;
canvas.height = window.innerHeight;

let particles = [];

for (let i = 0; i < 60; i++) {
  particles.push({
    x: Math.random() * canvas.width,
    y: Math.random() * canvas.height,
    size: Math.random() * 2,
    speedX: (Math.random() - 0.5) * 0.5,
    speedY: (Math.random() - 0.5) * 0.5,
    glow: Math.random()
  });
}

function animateFireflies() {
  ctx.clearRect(0, 0, canvas.width, canvas.height);

  particles.forEach(p => {
    p.x += p.speedX;
    p.y += p.speedY;

    p.glow += 0.05;
    const alpha = Math.abs(Math.sin(p.glow));

    ctx.beginPath();
    ctx.arc(p.x, p.y, p.size + 1, 0, Math.PI * 2);
    ctx.fillStyle = `rgba(255, 223, 100, ${alpha})`;
    ctx.fill();
  });

  requestAnimationFrame(animateFireflies);
}

animateFireflies();